    private void loadDataToTable(){
        try{
            prPhieuBaoHanh kt1 = new prPhieuBaoHanh();
            prBaoHanhSP kt2 = new prBaoHanhSP();
            
            List<PhieuBaoHanh> list1 = kt1.FindAll();
            List<BaoHanhSP> list2 = kt2.FindAll();
            tblModel1.setRowCount(0);
            for(PhieuBaoHanh it :list1){
                tblModel1.addRow(new Object[]{
                it.getMaPBH(), it.getNgaybatdau(),it.getNgayhethan(), 
                });
            }
            tblModel1.fireTableDataChanged();
            tblModel2.setRowCount(0);
            for(BaoHanhSP it :list2){
                tblModel2.addRow(new Object[]{
                it.getMaBH(), it.getMaSP(),it.getNgayBH(), it.getChiphi(),it.getMotaLoi()
                });
            }
            tblModel2.fireTableDataChanged();
            
        }catch(Exception e){
            e.printStackTrace();
            ThongBao.showLoi(parentForm, e.getMessage(), "Lỗi");
        }
    }
    
    private String getmaBH(){
        int count =0;
        prBaoHanhSP kt = new prBaoHanhSP();
        try {
            count = kt.somaBH();
        } catch (Exception ex) {
            Logger.getLogger(QuanLyBanHang.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(count<9){
            return "BH00" +(count+1);
        }else if(count>=9 && count<99){
            return "BH" + 0 +(count+1);
        }else
            return "BH"  +(count+1);
    }
    
    private String getmaPBH(){
        int count =0;
        prPhieuBaoHanh kt = new prPhieuBaoHanh();
        try {
            count = kt.somaPBH();
        } catch (Exception ex) {
            Logger.getLogger(QuanLyBanHang.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(count<9){
            return "PBH" + 0 +(count+1);
        } else if(count>9 && count<99)
            return "PBH" +(count+1);
        return null;
    }
