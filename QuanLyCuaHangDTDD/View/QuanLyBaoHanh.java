    private void btnXoaPBHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaPBHActionPerformed
        StringBuilder sb =new StringBuilder();
        KiemTraDuLieu.DuLieuTrong(txtMaPBH, sb, "Mã phiếu bảo hành trống!!");
        if(sb.length()>0){
            ThongBao.showLoi(parentForm, sb.toString(), "Lỗi!!");
            return;
        }
        if(ThongBao.showXacNhan(parentForm, "Bạn có muốn xóa phiếu bảo hành này không?", "Hỏi")== JOptionPane.NO_OPTION){
            return;
        }
        try{
            prPhieuBaoHanh kt = new prPhieuBaoHanh();
            if(kt.delete(txtMaPBH.getText())){
                ThongBao.showThongBao(parentForm, "Phiếu bảo hành đã được xóa", "Thông báo");
                btnThemPBHActionPerformed(evt);
                loadDataToTable();
            }else{
                ThongBao.showXacNhan(parentForm, "Lỗi!! Không thể xóa.", "Cảnh báo");
            }
            
        }catch(Exception e){
            e.printStackTrace();
            ThongBao.showLoi(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_btnXoaPBHActionPerformed

