CREATE OR REPLACE TRIGGER check_update_soluongNV_forinsert
BEFORE INSERT ON NHANVIEN
FOR EACH ROW
DECLARE
    slnv PHONGBAN.SoLuongNV%TYPE;
BEGIN
    SELECT COUNT(MaNV) INTO slnv
    FROM NHANVIEN
    WHERE MaPB=:NEW.MaPB
    GROUP BY :NEW.MaPB;
