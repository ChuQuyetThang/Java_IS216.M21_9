--TAO BANG PHONGBAN
CREATE TABLE PHONGBAN(
    MaPB CHAR(5) NOT NULL PRIMARY KEY,
    TenPB VARCHAR2(30),
    SoLuongNV NUMBER,
    TruongPB CHAR(5)
);

--TAO BANG SANPHAM
CREATE TABLE SANPHAM(
    MaSP CHAR(5) NOT NULL PRIMARY KEY,
    MaKM CHAR(5),
    MaNCC CHAR(5),
    TenSP VARCHAR2(30),
    MauSac VARCHAR2(10),
    DonGia NUMBER,
    SoLuong NUMBER
);

--TAO BANG KHUYENMAI
CREATE TABLE KHUYENMAI(
    MaKM CHAR(5) NOT NULL PRIMARY KEY,
    TenKM VARCHAR2(25),
    NgayBD DATE,
    NgayKT DATE,
    PhanTramKM NUMBER
);

--TAO BANG NHACUNGCAP 
CREATE TABLE NHACUNGCAP(
    MaNCC CHAR(5) NOT NULL PRIMARY KEY,
    TenNCC VARCHAR2(100),
    DiaChi VARCHAR2(100),
    Email VARCHAR2(50),
    SDT VARCHAR2(10)
);

--TAO BANG KHACHHANG
CREATE TABLE KHACHHANG(
    MaKH CHAR(5) NOT NULL PRIMARY KEY,
    TenKH VARCHAR2(30),
    DiaChi VARCHAR2(100),
    CMND VARCHAR2(12),
    Email VARCHAR2(50),
    SDT VARCHAR2(10)
);

--TAO BANG HD_BANSP
CREATE TABLE HD_BANSP(
    SoHD CHAR(5) NOT NULL PRIMARY KEY,
     MaKH CHAR(5),
    MaPBH CHAR(5),
    MaNV CHAR(5),
    NgayLapHD DATE,
    TriGia NUMBER
);

--TAO BANG CHI TIET HOA DON
CREATE TABLE CTHD_BANSP(
    SoHD CHAR(5),
    MaSP CHAR(5),
    SoLuongMua NUMBER,
    CONSTRAINT PK_CTHD PRIMARY KEY (SOHD,MASP)
);

--TAO BANG PHIEUBAOHANH 
CREATE TABLE PHIEUBAOHANH(
    MaPBH CHAR(5) NOT NULL PRIMARY KEY,
    NgayBatDau DATE,
    NgayHetHan DATE
);

--TAO BANG BAOHANHSP
CREATE TABLE BAOHANHSP(
    MaBH CHAR(5) NOT NULL PRIMARY KEY,
    MaSP CHAR(5),
    NgayBH DATE,
    ChiPhiBH NUMBER,
    MoTaLoi VARCHAR2(100)
);

--TAO BANG DANGNHAP
CREATE TABLE DANGNHAP(
    TenDN VARCHAR2(20) NOT NULL PRIMARY KEY,
    CapBac VARCHAR2(20),
    MatKhau VARCHAR2(20),
    MaNV CHAR(5)
);


