    ELSE
        DBMS_OUTPUT.PUT_LINE('Them nhan vien thanh cong');
        UPDATE PHONGBAN SET SoLuongNV=SoLuongNV+1 WHERE MaPB=:NEW.MaPB;
    END IF;
END;


